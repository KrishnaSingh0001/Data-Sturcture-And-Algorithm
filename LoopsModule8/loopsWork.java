package LoopsModule8;
import java.util.*;
public class loopsWork {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the number n:- ");
        int n = sc.nextInt();
        for(int i = 1 ; i<=n ; i++){
            System.out.println("Hello");
        }
        System.out.println(n);
    }
    
}
